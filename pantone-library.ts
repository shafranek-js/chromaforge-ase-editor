// Interface matching the JSON structure
export interface PantoneEntry {
    Code: string;
    C: string;
    M: string;
    Y: string;
    K: string;
    R: string;
    G: string;
    B: string;
    Hex: string;
}

// Build optimized lookup map from the large dataset
// Stores the full entry now, not just the hex string
const LIBRARY_MAP = new Map<string, PantoneEntry>();

// Populate map once
PANTONE_DATA.forEach(entry => {
    if (entry.Code && entry.Hex) {
        // Normalize the key: lowercase, no spaces/symbols
        const key = normalizeName(entry.Code);
        LIBRARY_MAP.set(key, entry);
    }
});

/**
 * Normalizes Pantone names to ensure matching works 
 * (e.g. "PANTONE Blue 0821 C" -> "blue0821")
 */
export function normalizeName(name: string): string {
    let n = name.toLowerCase();
    
    // Remove "pantone" or "pms" prefix if present
    n = n.replace(/^(pantone|pms)\s*/, '');
    
    // Remove common suffixes (C, U, CV, CVC, CP, X, TCX, TPX, etc)
    // We aggressively strip these to match the "Base" code in the library
    // e.g. "185 C" -> "185"
    const suffixes = ['cvc', 'cv', 'cp', 'tcx', 'tpx', 'c', 'u', 'x', 'm'];
    
    // Remove non-alphanumeric characters first to handle "185 C" -> "185c"
    n = n.replace(/[^a-z0-9]/g, '');

    // Check for suffix at end of string
    for (const suffix of suffixes) {
        if (n.endsWith(suffix)) {
            // Ensure we don't strip parts of actual names (unlikely with these specific codes)
            // but for short codes like "Black 6 C", "black6c" -> "black6"
            // check if stripping leaves nothing? No, codes are usually robust.
            const stem = n.substring(0, n.length - suffix.length);
            // Guard against stripping "black" -> "bla" if "ck" was a suffix
            if (stem.length > 0) { 
                n = stem;
                break; // Only strip one known suffix
            }
        }
    }
    
    return n;
}

/**
 * Look up a color name in the authority library.
 */
export function getOfficialHex(name: string): string | null {
    if (!name) return null;
    
    // 1. Check Manual Overrides (Exact Match first)
    if (PANTONE_OVERRIDES[name]) {
        return PANTONE_OVERRIDES[name];
    }

    // 2. Check Data Library (Normalized)
    const normalizedInput = normalizeName(name);
    const entry = LIBRARY_MAP.get(normalizedInput);
    
    if (entry) {
        return entry.Hex;
    }
    
    // 3. Check Manual Overrides (Normalized keys) as fallback
    const overrideKey = Object.keys(PANTONE_OVERRIDES).find(k => 
        normalizeName(k) === normalizedInput
    );
    
    if (overrideKey) {
        return PANTONE_OVERRIDES[overrideKey];
    }

    return null;
}

/**
 * Get the full data entry for validation purposes
 */
export function getOfficialEntry(name: string): PantoneEntry | null {
    if (!name) return null;
    const normalizedInput = normalizeName(name);
    return LIBRARY_MAP.get(normalizedInput) || null;
}
